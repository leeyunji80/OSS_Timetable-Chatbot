import pandas as pd
import io
from itertools import combinations

def parse_day_and_period_to_indices(day_raw, period_raw):
    """
    요일('월~금')과 교시("06,07") 문자열을 표(Table)에 매핑할 수 있는 인덱스로 변환하는 함수
    """
    if pd.isna(day_raw) or pd.isna(period_raw):
        return []
        
    day_mapping = {'월': 0, '화': 1, '수': 2, '목': 3, '금': 4, '토': 5}
    time_indices = []
    
    # 파이프(|)를 기준으로 다중 요일/교시 분할
    day_splits = str(day_raw).split('|')
    period_splits = str(period_raw).replace('"', '').split('|')
    
    for day_chunk, period_chunk in zip(day_splits, period_splits):
        day_str = day_chunk.strip()
        day_idx = day_mapping.get(day_str)
        if day_idx is None:
            continue
            
        # 교시를 숫자로 변환 후 표 인덱스(0부터 시작)에 맞추기 위해 -1
        periods = [int(p.strip()) - 1 for p in period_chunk.split(',') if p.strip().isdigit()]
        
        for period_idx in periods:
            time_indices.append({
                "day_idx": day_idx,       # 표의 '열(Column)' 위치 (0=월, 1=화...)
                "period_idx": period_idx  # 표의 '행(Row)' 위치 (0=1교시, 1=2교시...)
            })
            
    return time_indices

def generate_timetable_combinations(csv_data, target_grade, num_to_pick=5, exclude_days=None):
    # 1. 데이터 로드
    df = pd.read_csv(io.StringIO(csv_data))
    
    # 2. 기본 필터링 (학년 및 야간 강좌 제외)
    # LLM이 "2학년꺼 짜줘"라고 하면 해당 학년만 남깁니다.
    mask = df['수강 대상'].str.contains(target_grade) & ~df['수강 대상'].str.contains("야간")
    filtered_df = df[mask].copy()
    
    # 3. 사용자 요구사항 반영 (특정 요일 제외)
    # 예: "금요일 공강 만들어줘" -> 금요일 수업이 포함된 행 삭제
    if exclude_days:
        for day in exclude_days:
            filtered_df = filtered_df[~filtered_df['요일'].str.contains(day, na=False)]
    
    # 4. 과목명 리스트 추출
    course_pool = []
    for _, row in filtered_df.iterrows():
        # 요일과 교시 컬럼 데이터를 바탕으로 표 인덱스 리스트 추출
        time_slots = parse_day_and_period_to_indices(row['요일'], row['교시'])
        
        course_pool.append({
            "code": row['교과목 번호'],
            "name": row['교과목명'],
            "professor": row['담당교수'],
            "room": row['강의실'],
            "day_raw": row['요일'],
            "period_raw": row['교시'],
            "time_slots": time_slots  # [{'day_idx': 0, 'period_idx': 5}, ...] 구조
        })
    
    # 5. 시간표 조합 생성
    # pool에 있는 과목들 중 사용자가 원하는 개수(num_to_pick)만큼 뽑는 모든 경우의 수
    all_combinations = list(combinations(course_pool, num_to_pick))
    
    # 6. 결과 반환 (리스트의 리스트 형태)
    # 이 결과값을 '충돌 방지 로직' 브랜치에 있는 함수에 넣어서 검증하면 됩니다.
    return [list(combo) for combo in all_combinations]

